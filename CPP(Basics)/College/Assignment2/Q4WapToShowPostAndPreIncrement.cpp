# include <bits/stdc++.h>
 using  namespace  std;
 int  main(){
    int  a= 10;
    cout<<"Kanishk"<<endl;
    cout<<"07129802021"<<endl;
    cout<<"Post increment ("<<a<<"): "<<a++<<endl;
    a=10;
    cout<<"Pre increment ("<<a<<"): "<<++a<<endl;
    a=10;
    cout<<"Post decrement ("<<a<<"): "<<a--<<endl;
    a=10;
    cout<<"Pre decrement ("<<a<<"): "<<--a<<endl;
    return   1 ;
}